    sample_pb = UInt8[
        0x0a,0xee,0x01,0x0a,0x0c,0x73,0x61,0x6d,0x70,0x6c,0x65,0x2e,0x70,0x72,0x6f,0x74,
        0x6f,0x12,0x06,0x73,0x61,0x6d,0x70,0x6c,0x65,0x22,0x15,0x0a,0x05,0x49,0x6e,0x6e,
