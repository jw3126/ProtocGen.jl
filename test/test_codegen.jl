    # test_bootstrap_descriptors.jl, for the proto:
    #     syntax = "proto3";
    #     package sample;
    #     message Inner { int32 a = 1; }
    #     message Outer {
    #       string name = 1;
    #       optional int32 maybe = 2;
    #       Inner nested = 3;
    #       oneof choice { int32 ci = 4; string cs = 5; }
    #       repeated int64 packed_ints = 6;
    #     }
    sample_pb = UInt8[
        0x0a,0xee,0x01,0x0a,0x0c,0x73,0x61,0x6d,0x70,0x6c,0x65,0x2e,0x70,0x72,0x6f,0x74,
