    # Run the captured FileDescriptorProto through the plugin.
    request = GC.CodeGeneratorRequest(
        ["sample.proto"],
        nothing,
        fdset.file,
        G.FileDescriptorProto[],
        nothing,
    )
    req_io = IOBuffer()
    ProtoBufDescriptors.encode(ProtoBufDescriptors.ProtoEncoder(req_io), request)
